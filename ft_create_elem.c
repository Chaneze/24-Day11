#include "ft_list.h"

t_list	*ft_create_elem(void *data)
{
	t_list	*new;

	new = NULL;
	new = (t_list*)malloc(sizeof(t_list));
	if (new)
	{
		new->data = data;
		new->next = NULL;
	}
	return (new);
}

int		main(void)
{
	t_list *maillon;

	maillon = ft_create_elem("5");
	printf("%s\n", maillon->data);
	return (0);
}
